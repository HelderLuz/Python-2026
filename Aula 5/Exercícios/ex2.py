# Crie dois números e realize as operações de soma, subtração, multiplicação, divisão, divisão inteira e módulo com esses números.

a = 15
b = 7

print(f'A: {a} | B: {b}')
print(f'Soma: {a + b}')
print(f'Substração: {a - b}')
print(f'Multiplicação: {a * b}')
print(f'Divisão: {(a / b):.4f}')
print(f'Divisão inteira: {a // b}')
print(f'Resto (módulo): {a % b}')